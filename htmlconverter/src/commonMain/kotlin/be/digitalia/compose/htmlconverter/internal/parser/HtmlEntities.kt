    private fun unicodeToString(c: Int): String {
        return if (c > 0xffff) {
            // write high Unicode value as surrogate pair
            val offset = c - 0x010000
            charArrayOf(
                ((offset ushr 10) + 0xd800).toChar(),   // high surrogate
                ((offset and 0x3ff) + 0xdc00).toChar()  // low surrogate
            ).concatToString()
        } else {
            c.toChar().toString()
        }
    }

    val entityResolver: (String) -> String? = { entityName ->
        val index = ENTITY_NAMES.binarySearch(entityName)
        if (index < 0) null else unicodeToString(ENTITY_CODES[index])
    }
